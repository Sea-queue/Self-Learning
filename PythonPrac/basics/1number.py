# functions on numbers
from math import floor
from math import ceil
from math import sqrt

num = -7
# use str() to print number along with string
print(str(num) + " is my favorite number")

print(abs(num))         # 7
print(max(4, 5, 19))    # 19
print(min(4, 5, 19))    # 4
print(round(3.4))       # 3
print(round(3.7))       # 4
print(pow(6, 2))        # 36

# has to import math: "from math import *"
print(floor(3.5))       # 3
print(ceil(3.5))        # 4
print(sqrt(36))         # 6.0
